package com.tikal.aeronautikal.model.otBody;

public class Seccion {
	private String nombre;
	private int numero;
	/**
	 * Recupera el valor del campo nombre para su uso.
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * Establece el valor del campo nombre.
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * Recupera el valor del campo numero para su uso.
	 * @return the numero
	 */
	public int getNumero() {
		return numero;
	}
	/**
	 * Establece el valor del campo numero.
	 * @param numero the numero to set
	 */
	public void setNumero(int numero) {
		this.numero = numero;
	}

}
